#include <time.h>

/*
Funções de Tempo

clock() - Retorna o número de pulsos de clock decorridos desde que o programa foi lançado.
difftime(t1, t2) - Retorna a diferença entre t1 e t2, em segundos.
mktime(t) - Converte a estrutura t em um valor de tempo.
time(t) - Retorna o tempo atual.
localtime(t) - Converte o tempo t em uma estrutura tm representando a hora local.
asctime(t) - Converte a estrutura tm t em uma string representando a hora local.
ctime(t) - Converte o tempo t em uma string representando a hora local.
strftime(s, n, f, t) - Formata o tempo t de acordo com o formato f e armazena o resultado em s.
*/